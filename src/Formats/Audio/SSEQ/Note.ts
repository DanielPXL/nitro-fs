const noteToFrequencies: number[] = []

for (let i = 0; i < 128; i++) {
	noteToFrequencies[i] = 440 * Math.pow(2, (i - 69) / 12)
}

export function noteToFrequency(note: Note) {
	return noteToFrequencies[note]
}